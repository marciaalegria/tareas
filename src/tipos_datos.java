public class tipos_datos {
        public static void main(String[] args) {

            char letra = 'R';
            long numeroLargo = 1495970192837664L;
            float decimal = 12.5F;
            boolean verdaderoFalso = true;
            int entero = 90;
            String cadenaCaracteres = "No tengo trono ni reina, ni nadie que me comprenda, pero sigo siendo el rey";
            double decimalLargo = 6.626070040;

            System.out.println("Respuesta A : " + letra);
            System.out.println("Respuesta B : " + numeroLargo);
            System.out.println("Respuesta C : " + decimal);
            System.out.println("Respuesta D : " + verdaderoFalso);
            System.out.println("Respuesta E : " + entero);
            System.out.println("Respuesta F : " + cadenaCaracteres);
            System.out.println("Respuesta G : " + decimalLargo);
        }
    }
