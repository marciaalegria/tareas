public class variablesTipos {
    public static void main(String[] args) {
        byte myFavoriteNumber = 14;
        short myVariableShort = 1414;
        int myVariableInt = 1414141414;
        long myVariableLong = 1414141414L;

        System.out.println("mi numero favorito variable byte " + myFavoriteNumber);
        System.out.println("mi variable short " + myVariableShort);
        System.out.println("mi variable int " + myVariableInt);
        System.out.println("mi variable long " + myVariableLong);

        float variableFloat = 3.40f;
        double variableDouble = 52.6325d;
        System.out.println("mi variable float " + variableFloat);
        System.out.println("mi variable double " + variableDouble);

    }
}
